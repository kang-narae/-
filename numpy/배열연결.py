
import numpy as np


'''
hstack
행의 수가 같은 두 개 이상의 배열을 옆으로 연결하여 열의 수가 더 많은 배열을 만든다. 
연결할 배열은 하나의 리스트에 담아야 한다.'''
a1= np.array([[1., 1., 1.],        [1., 1., 1.]])
a2=np.array([[0., 0.],        [0., 3.]])

c= np.hstack([a1,a2])
'''[[1. 1. 1. 0. 0.]
 [1. 1. 1. 0. 3.]]'''
print(c)

